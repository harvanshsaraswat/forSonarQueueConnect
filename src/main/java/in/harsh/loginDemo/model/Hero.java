package in.harsh.loginDemo.model;

public class Hero {
private  int id;
private  String name;


public Hero() {
	super();
}
public Hero(int id, String name) {
          this.id = id;
          this.name = name;
    }
public int getId() {
          return id;
    }
public String getName() {
          return name;
    }
}
